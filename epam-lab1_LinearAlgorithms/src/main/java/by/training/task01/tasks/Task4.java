package by.training.task01.tasks;

public final class Task4 {
    /**
     * method finds the value of function z = (a - 3) * b / 2 + c
     * @param a - real number
     * @param b - real number
     * @param c - real number
     * @return value of the function z
     */
    public static double getFunctionValue(double a, double b, double c) {
        return (a - 3) * b / 2 + c;
    }
}
